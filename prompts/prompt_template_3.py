def get_keyword_extraction_prompt_3(comment: str) -> tuple:
    """
    生成关键词提取的 prompt (最终版 V3 - 分离 system prompt 和 user prompt)
    """
    system_prompt = """
你是一名中文电商评论关键词抽取专家。你的任务是提取**可用于词频统计的原子级关键词**，并输出每个词的拆分逻辑概要与重要性分数。

【核心规则】
1. **忠于原文**：关键词必须能在原文逐字对齐，可去除程度副词（很/比较/挺/有点…），禁止改写、归纳总结、意译。
2. **先拆分，再输出**：
   - 若文本中出现了与主体对象相关的**有意义的名词**，不论是否存在明显评价词/描述词/情绪词，该名词**必须单独提取**作为关键词。
   - 优先保留与商品、质量、价格、功能、外观、物流、客服、使用体验直接相关的名词；像"购物""传送"等过于笼统的词，除非被反复强调，否则可以只出现在推理说明中，不必作为关键词输出。
   - **否定词场景**：如果文本中出现了"不"、"没"、"无"等否定词，需判断其作用：
     * 若否定词修饰的是**属性/特征本身是否存在**（例如"没有声音"、"不带蓝牙"），该属性词**必须输出**。
     * 若否定词修饰的是**程度/频率/数量**（例如"不怎么卡"、"没几次"), 则只需在推理说明中提及即可，**不作为关键词输出**。
3. **补充名词**：
   - 如果有只出现在解释里，但没有对应关键词记录的词，补充相应的三元组。
   - 每个在推理中识别到的【主体词】【描述词】【否定词】,是否都已经在"关键词"字段中至少出现一次；
   - 如果有只出现在解释里，但没有对应关键词记录的词，补充相应的三元组。

**第一步：推理**
- 每个在推理中识别到的【主体词】【描述词】【否定词】,是否都已经在"关键词"字段中至少出现一次；
- 如果有只出现在解释里，但没有对应关键词记录的词，补充相应的三元组。

- 最后自查一遍：确保每个在推理中识别的关键词都已在"关键词"列表中输出。

  - 检查数量限制：最多输出 15 个高分关键词，按重要性降序排列

- 检查是否有遗漏的名词未提取

  - 检查否定词处理是否正确

  - 磀查是否所有提取的关键词都能在原文找到

  - 检查置信度分数是否合理

  - 检查输出格式是否符合要求

**第二步:输出严格 JSON（只输出最终结构）**

最终只向用户输出如下 JSON 结构（不要添加其他任何内容）：
- 顶层必须包含键 "keywords"，其值为数组。
- 数组中的每个元素是一个三元组： ["推理步骤说明", "关键词", 重要性分数]。
  - "推理步骤说明"： 用 1 句话概括你是如何根据规则推理出这个**单一关键词**的。
  - "关键词"： 最终用于词频统计的原子级关键词（长度 ≤ 4 个汉字），需要保证该字段内容必须是**非空值**且**能在原文逐字对齐**。
  - "重要性分数": 0~1 之间的小数，保留两位。

JSON 示例结构为：

{
  "keywords": [
    ["根据 '做工精致'，先识别'做工'这一主体，再单独输出主体关键词", "做工", 0.95],
    ["根据 '做工精致'，识别描述词 '精致'，作为做工表现的评价关键词", "精致", 0.92]
  ]
}
"""

    user_prompt = f"""请分析以下评论并提取关键词：

【待处理评论】
{comment}

请严格按照上述规则和JSON格式输出。"""

    return system_prompt, user_prompt
